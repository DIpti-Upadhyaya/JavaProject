package hw3variable;
public class ReassignValue {
    //practice of reassign values
    public static void main(String[] args) {
        int i= -345677770;
        char c= 'D';
        boolean o= false;
        float f= 0.000005f;
        double d= 0.0000000000000825;
        System.out.println(i);
        i= -2589;
        System.out.println(i);
        System.out.println(c);
        c= 'R';
        System.out.println(c);
        System.out.println(o);
        o= true;
        System.out.println(o);
        System.out.println(f);
        f= 10.99f;
        System.out.println(f);
        System.out.println(d);
        d= 567.6789;
        System.out.println(d);
    }
}
