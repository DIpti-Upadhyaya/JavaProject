package hw3variable;

public class PrimitiveDataType {
    //practice of Datatype initial value
    public static void main(String[] args) {
        byte b =127; // testing of byte value -128 to 127
        short s = 32765; //initial value of short -32768 t0 32767
        int i= -345677770;  //value of integer  up to 9 digit
        long l= -1111111111111111111L; // to define long number you must write L at the back
        char c= 'D'; //only one character must be written within ' '
        boolean o= false; //only define true / false conditional data type
        float f= 0.000005f; // any decimal value, must write f/F at the back
        double d= 0.0000000000000825; //any decimal value
        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(l);
        System.out.println(c);
        System.out.println(o);
        System.out.println(f);
        System.out.println(d);
    }



}
